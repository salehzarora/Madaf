-- Denormalize order totals from the lines. VAT is computed ORDER-LEVEL
-- (round(subtotal × rate)) to match how the M0 UI displays estimates
-- (document-view.tsx: subtotal * VAT_RATE) — per-line line_vat values are
-- informational and may differ from the order total by ±0.01 in sum.
update public.orders o
set subtotal = s.sub,
    vat_total = round(s.sub * 0.18, 2),
    total = s.sub + round(s.sub * 0.18, 2)
from (
  select order_id,
         sum(line_subtotal) as sub
  from public.order_items
  group by order_id
) s
where s.order_id = o.id;

